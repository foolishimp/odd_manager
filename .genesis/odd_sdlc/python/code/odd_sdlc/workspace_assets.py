@dataclass(frozen=True)
class GeneratedAssetContract:
    asset_id: str
    materialization_kind: str
    heading_prefix: str | None = None
    required_files: tuple[str, ...] = ()
    required_member_prefixes: tuple[tuple[str, str], ...] = ()

    def to_dict(self) -> dict[str, object]:
        return {
            "asset_id": self.asset_id,
            "materialization_kind": self.materialization_kind,
            "heading_prefix": self.heading_prefix,
            "required_files": list(self.required_files),
            "required_member_prefixes": [
                {"path": relative_path, "prefix": prefix}
                for relative_path, prefix in self.required_member_prefixes
            ],
        }


