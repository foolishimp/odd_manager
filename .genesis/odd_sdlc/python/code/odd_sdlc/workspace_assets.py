def resolved_asset_relative_path(workspace_root: Path, asset_id: str) -> str:
    if asset_id in DYNAMIC_TENANT_ASSET_IDS:
        return _tenant_asset_relative_path(workspace_root, asset_id)
    if asset_id != "code_surface":
        return asset_relative_path(asset_id)
    return load_project_profile(workspace_root).code_relative_path()


def asset_path(workspace_root: Path, asset_id: str) -> Path:
    return workspace_root / resolved_asset_relative_path(workspace_root, asset_id)


def asset_materialization_path(workspace_root: Path, asset_id: str) -> Path:
    path = asset_path(workspace_root, asset_id)
    primary_member = ASSET_PRIMARY_MEMBERS.get(asset_id)
    if primary_member is None:
        return path
    return path.joinpath(*primary_member)


def asset_marker_path(workspace_root: Path, asset_id: str) -> Path:
    path = asset_materialization_path(workspace_root, asset_id)
    marker_member = ASSET_MARKER_MEMBERS.get(asset_id)
    if marker_member is None:
        return path
    return asset_path(workspace_root, asset_id).joinpath(*marker_member)


