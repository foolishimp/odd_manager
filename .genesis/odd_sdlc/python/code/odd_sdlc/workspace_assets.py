def assess_generated_asset_contract(workspace_root: Path, asset_id: str) -> dict[str, object]:
    contract = _resolved_generated_asset_contract(workspace_root, asset_id)
    project_profile = load_project_profile(workspace_root)
    root_path = asset_path(workspace_root, asset_id)
    materialization_path = asset_materialization_path(workspace_root, asset_id)
    marker_path = asset_marker_path(workspace_root, asset_id)
    marker_text = asset_marker(asset_id)

    heading_matches = True
    if contract.heading_prefix is not None:
        if asset_id in AUTHORITATIVE_FILE_HEADING_PREFIXES:
            heading_matches = _text_startswith_any(materialization_path, AUTHORITATIVE_FILE_HEADING_PREFIXES[asset_id])
        else:
            heading_matches = _text_startswith(materialization_path, contract.heading_prefix)

    missing_files = [
        relative_path
        for relative_path in contract.required_files
        if not (materialization_path / relative_path).is_file()
    ]
    member_prefix_failures = [
        {
            "path": relative_path,
            "expected_prefix": prefix,
        }
        for relative_path, prefix in contract.required_member_prefixes
        if not _text_startswith(materialization_path / relative_path, prefix)
    ]

    materialization_kind_actual = _path_kind(materialization_path)
    marker_present = _text_contains(marker_path, marker_text)
    code_summary: dict[str, object] | None = None
    adopted_authority = False
    if asset_id in AUTHORITATIVE_FILE_HEADING_PREFIXES and materialization_kind_actual == "file" and heading_matches:
        text = materialization_path.read_text(encoding="utf-8").strip()
        adopted_authority = bool(text)
        marker_present = marker_present or adopted_authority
    if asset_id == "code_surface" and project_profile.realization_mode in {"selected_output_tree", "planned_output_tree"}:
        code_summary = summarize_code_surface(workspace_root)
        marker_present = bool(code_summary["build_markers"]) or int(code_summary["source_file_count"]) > 0
    topology_report = assess_realization_topology(workspace_root)
    topology_guard_applied = asset_id in TOPOLOGY_GUARDED_ASSETS
    topology_guard_passed = not topology_guard_applied or not topology_report["topology_diverged"]
    contract_satisfied = (
        materialization_kind_actual == contract.materialization_kind
        and marker_present
        and heading_matches
        and not missing_files
        and not member_prefix_failures
        and topology_guard_passed
    )
    return {
        "asset_id": asset_id,
        "relative_path": resolved_asset_relative_path(workspace_root, asset_id),
        "root_path": str(root_path),
        "root_exists": root_path.exists(),
        "materialization_path": str(materialization_path),
        "materialization_kind_expected": contract.materialization_kind,
        "materialization_kind_actual": materialization_kind_actual,
        "exists": materialization_path.exists(),
        "marker_path": str(marker_path),
        "marker_present": marker_present,
        "heading_prefix": contract.heading_prefix,
        "heading_matches": heading_matches,
        "required_files": list(contract.required_files),
        "missing_files": missing_files,
        "required_member_prefixes": [
            {"path": relative_path, "prefix": prefix}
            for relative_path, prefix in contract.required_member_prefixes
        ],
        "member_prefix_failures": member_prefix_failures,
        "topology_guard_applied": topology_guard_applied,
        "topology_guard_passed": topology_guard_passed,
        "foreign_realization_candidates": topology_report["foreign_realization_candidates"],
        "project_profile": project_profile.to_dict(),
        "code_surface_summary": code_summary,
        "adopted_authority": adopted_authority,
        "contract": contract.to_dict(),
        "contract_satisfied": contract_satisfied,
    }


def _digest_file(path: Path) -> str:
    return hashlib.sha256(path.read_bytes()).hexdigest()


def _digest_directory(path: Path) -> str:
    digest = hashlib.sha256()
    for child in sorted(item for item in path.rglob("*") if item.is_file()):
        digest.update(child.relative_to(path).as_posix().encode("utf-8"))
        digest.update(b"\0")
        digest.update(_digest_file(child).encode("utf-8"))
        digest.update(b"\0")
    return digest.hexdigest()


def _directory_size(path: Path) -> int:
    return sum(child.stat().st_size for child in path.rglob("*") if child.is_file())


def checkpoint_for_path(path: Path) -> AssetCheckpoint:
    if not path.exists():
        return AssetCheckpoint(exists=False, path_kind="missing", content_digest=None, bytes=None)
    if path.is_dir():
        return AssetCheckpoint(
            exists=True,
            path_kind="directory",
            content_digest=_digest_directory(path),
            bytes=_directory_size(path),
        )
    return AssetCheckpoint(
        exists=True,
        path_kind="file",
        content_digest=_digest_file(path),
        bytes=path.stat().st_size,
    )


def bootstrap_assets(workspace_root: Path) -> tuple[Asset, ...]:
    assets: list[Asset] = []
    project_profile = load_project_profile(workspace_root)
    for node_name, _ in ASSET_PATHS:
        rel_path = resolved_asset_relative_path(workspace_root, node_name)
        path = asset_path(workspace_root, node_name)
        asset_type = asset_declared_type(node_name)
        type_profile = ASSET_TYPES[asset_type]
        checkpoint = checkpoint_for_path(path)
        metadata = {
            "relative_path": rel_path,
            "exists": "true" if path.exists() else "false",
            "path_kind": checkpoint.path_kind,
        }
        if node_name == "code_surface":
            metadata.update(
                {
                    "tenant_name": project_profile.tenant_name,
                    "realization_mode": project_profile.realization_mode,
                    "language": project_profile.language,
                    "test_runner": project_profile.test_runner,
                }
            )
        assets.append(
            Asset(
                asset_id=node_name,
                uri=relative_file_uri(path, workspace_root=workspace_root),
                declared_type=asset_type,
                metadata=metadata,
                provenance=AssetProvenance(
                    model="projected_checkpoint",
                    source="workspace_materialized_state",
                    mutable=type_profile.mutable_default,
                    history_basis="runtime_and_constructive_event_history",
                ),
                checkpoint=checkpoint,
            )
        )
    return tuple(assets)


def bootstrap_input_collection(workspace_root: Path) -> AssetCollection:
    return AssetCollection(name="bootstrap_input_set", assets=bootstrap_assets(workspace_root))


def bootstrap_bindings(workspace_root: Path) -> tuple[AssetNodeBinding, ...]:
    assets = {asset.asset_id: asset for asset in bootstrap_assets(workspace_root)}
    bindings = [
        AssetNodeBinding(node="input_set", asset_ids=INPUT_SET_ASSET_IDS),
    ]
    for node_name, _ in ASSET_PATHS:
        bindings.append(AssetNodeBinding(node=node_name, asset_ids=(node_name,)))
    return tuple(bindings)
