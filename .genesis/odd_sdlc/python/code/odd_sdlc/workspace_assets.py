def _path_kind(path: Path) -> str:
    if not path.exists():
        return "missing"
    if path.is_dir():
        return "directory"
    return "file"


def _text_contains(path: Path, needle: str) -> bool:
    return path.exists() and path.is_file() and needle in path.read_text(encoding="utf-8")


def _text_startswith(path: Path, prefix: str) -> bool:
    return path.exists() and path.is_file() and path.read_text(encoding="utf-8").startswith(prefix)


def _text_startswith_any(path: Path, prefixes: tuple[str, ...]) -> bool:
    if not path.exists() or not path.is_file():
        return False
    text = path.read_text(encoding="utf-8")
    return any(text.startswith(prefix) for prefix in prefixes)


def _is_realization_file(path: Path, *, root: Path) -> bool:
    relative_parts = path.relative_to(root).parts
    return (
        path.suffix in SOFTWARE_SOURCE_SUFFIXES
        and not any(part in IGNORED_REALIZATION_DIR_NAMES for part in relative_parts)
    )


def _test_report_count(root: Path) -> int:
    seen: set[Path] = set()
    for pattern in TEST_REPORT_PATTERNS:
        for path in root.glob(pattern):
            if path.is_file():
                seen.add(path)
    return len(seen)


def summarize_code_surface(workspace_root: Path) -> dict[str, object]:
    code_root = asset_path(workspace_root, "code_surface")
    build_markers = [marker for marker in SOFTWARE_BUILD_MARKERS if (code_root / marker).is_file()]
    source_files = [
        path
        for path in code_root.rglob("*")
        if path.is_file() and _is_realization_file(path, root=code_root)
    ] if code_root.exists() and code_root.is_dir() else []
    test_source_files = [
        path
        for path in source_files
        if "test" in {part.lower() for part in path.relative_to(code_root).parts}
    ]
    return {
        "relative_path": resolved_asset_relative_path(workspace_root, "code_surface"),
        "build_markers": build_markers,
        "source_file_count": len(source_files),
        "test_source_file_count": len(test_source_files),
        "test_report_file_count": _test_report_count(code_root) if code_root.exists() and code_root.is_dir() else 0,
    }


def summarize_test_evidence(workspace_root: Path) -> dict[str, object]:
    code_root = asset_path(workspace_root, "code_surface")
    profile = load_project_profile(workspace_root)
    report_files: list[Path] = []
    if code_root.exists() and code_root.is_dir():
        seen: set[Path] = set()
        for pattern in TEST_REPORT_PATTERNS:
            for path in code_root.glob(pattern):
                if path.is_file() and path not in seen:
                    seen.add(path)
                    report_files.append(path)
    foreign_report_files: list[Path] = []
    seen_foreign: set[Path] = set()
    selected_code_root_name = _selected_code_root_name(workspace_root)
    for child in sorted(workspace_root.iterdir()):
        if not child.is_dir() or child.name in GOVERNANCE_TOP_LEVEL_NAMES or child.name == selected_code_root_name:
            continue
        for pattern in TEST_REPORT_PATTERNS:
            for path in child.glob(pattern):
                if path.is_file() and path not in seen_foreign:
                    seen_foreign.add(path)
                    foreign_report_files.append(path)
    active_build_tenant_root = _selected_build_tenant_root(workspace_root)
    build_tenants_root = workspace_root / "build_tenants"
    if build_tenants_root.exists() and build_tenants_root.is_dir():
        for tenant_root in sorted(build_tenants_root.iterdir()):
            if not tenant_root.is_dir():
                continue
            if active_build_tenant_root is not None and tenant_root == active_build_tenant_root:
                continue
            for pattern in TEST_REPORT_PATTERNS:
                for path in tenant_root.glob(pattern):
                    if path.is_file() and path not in seen_foreign:
                        seen_foreign.add(path)
                        foreign_report_files.append(path)
    tests = 0
    failures = 0
    errors = 0
    skipped = 0
    parsed_reports = 0
    for report in report_files:
        try:
            root = ElementTree.fromstring(report.read_text(encoding="utf-8"))
        except Exception:
            continue
        suites = [root] if root.tag == "testsuite" else list(root.findall(".//testsuite"))
        if not suites:
            continue
        parsed_reports += 1
        for suite in suites:
            tests += int(suite.attrib.get("tests", "0") or "0")
            failures += int(suite.attrib.get("failures", "0") or "0")
            errors += int(suite.attrib.get("errors", "0") or "0")
            skipped += int(suite.attrib.get("skipped", "0") or "0")
    report_paths = [
        path.relative_to(workspace_root).as_posix()
        for path in sorted(report_files)
    ]
    ungoverned_report_paths = [
        path.relative_to(workspace_root).as_posix()
        for path in sorted((*report_files, *foreign_report_files))
    ]
    if not profile.has_test_execution_capability():
        return {
            "report_file_count": 0,
            "parsed_report_count": 0,
            "tests": 0,
            "failures": 0,
            "errors": 0,
            "skipped": 0,
            "report_paths": [],
            "ungoverned_report_file_count": len(ungoverned_report_paths),
            "ungoverned_report_paths": ungoverned_report_paths,
        }
    return {
        "report_file_count": len(report_files),
        "parsed_report_count": parsed_reports,
        "tests": tests,
        "failures": failures,
        "errors": errors,
        "skipped": skipped,
        "report_paths": report_paths,
        "ungoverned_report_file_count": len(foreign_report_files),
        "ungoverned_report_paths": [
            path.relative_to(workspace_root).as_posix()
            for path in sorted(foreign_report_files)
        ],
    }


def _selected_code_root_name(workspace_root: Path) -> str | None:
    selected = asset_path(workspace_root, "code_surface")
    try:
        relative = selected.relative_to(workspace_root)
    except ValueError:
        return None
    return relative.parts[0] if relative.parts else None


def _selected_build_tenant_root(workspace_root: Path) -> Path | None:
    selected = asset_path(workspace_root, "code_surface")
    try:
        relative = selected.relative_to(workspace_root)
    except ValueError:
        return None
    if len(relative.parts) >= 2 and relative.parts[0] == "build_tenants":
        return workspace_root / relative.parts[0] / relative.parts[1]
    return None


